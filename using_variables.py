#f-strings

# Define variables
age = 25
height = 175
name = "Petter"

# Output using f-strings
print(f"My name is {name}, I am {age} years old and I am {height} cm tall. ")


#int 

age = 25
height = 175
name = "Petter"

print("My name is {}, I'm {} years old and I am {height} cm tall. ".format(name, age, height))


#str

age = 25
height = 1.75
name = "John"

print("My name is " + str(name) + ", I am " + str(age) + " years old and I am" + str(height) + " cm tall. ")
